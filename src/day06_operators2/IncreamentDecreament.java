package day06_operators2;

public class IncreamentDecreament {
public static void main(String[] args) {

int employees = 50;
System.out.println("Employees: "+ employees );

employees++; // add 1 , increment by 1 =  51
employees++; // add 1 , increment by 1 = 52
employees+=1; // add 1 , increment by 1 = 53
employees = employees +1; // add 1 , increment by 1 = 54
System.out.println("Employees: "+employees);
	
	employees--; // mines 1 / decrease by 1. = 53
	--employees; // mines 1. = 52
	
	System.out.println("Employees: "+employees);
	
	
	
}
	

}
