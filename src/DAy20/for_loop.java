package DAy20;

public class for_loop {
	public static void main(String[] args) throws InterruptedException {
		
		// using for loop print loop java "love java" 10 times
		
	for (int i = 1; i <= 10; i++ ) {
		System.out.println("Love java");
	}
	
	// print 5-15 using for loop
	
	for ( int i1 = 5; i1 <= 15; i1++) {
		System.out.println(i1);
	}
	
	for ( int i2 = 20; i2 >= 10; i2--) {
		System.out.print(i2+" ");
		Thread.sleep(456);
	}
	
	}
}
